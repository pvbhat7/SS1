package com.sdgvvk.v1.modal;

public class ContactViewedModal {
    private String vcpid;
    private String name;
    private String city;
    private String photo;
    private String mobile;
    private String viewedOn;
    private String dob;

    public String getVcpid() {
        return vcpid;
    }

    public void setVcpid(String vcpid) {
        this.vcpid = vcpid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getViewedOn() {
        return viewedOn;
    }

    public void setViewedOn(String viewedOn) {
        this.viewedOn = viewedOn;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }
}
